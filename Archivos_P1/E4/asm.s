Reset_Handler
        LDR     R0, =0x10000000
		MOVS	R5,#0; PONEMOS A 0 ESE REGISTRO, PARA USARLO COMO AUXILIAR
		LDRSH	R1,[R0,R5];DATO A: MOVEMOS EL CONTENIDO DE LA DIRECCION APUNTADA POR R0 MAS R5(0), AL REGISTRO 1 
		ADDS	R0,#4;AUMENTAMOS R0 PARA APUNTAR AL SIGUIENTE DATO
		LDRSH	R2,[R0,R5];DATO B: MOVEMOS EL CONTENIDO DE LA DIRECCION APUNTADA POR R0 MAS R5(0). AL REGISTRO 2
		SUBS	R3,R1,R2;A-B
		ASRS 	R3,#1; DIVIDIMOS EL RESULTADO DE LA RESTA ENTRE 2
		MOVS	R4,R1;MOVEMOS EL CONTENIDO DER1(DATO A) A R4, PARA NO PISAR NINGUN DATO
		MULS    R4,R2,R4; A*B
		ASRS	R4,#2; DIVIDIMOS EL RESULTADO DE LA MULTIPLICACION ENTRE 4
		ADDS	R0,#12; APUNTAMOS R0 A 10..10 
		STR		R3, [R0]; MOVEMOS EL CONTENIDO DE R3 A LA DIRECCION APUNTADA POR R0
		ADDS	R0, #4 ; APUNTAMOS R0 A 100..14 
		STR		R4, [R0]; MOVEMOS EL CONTENIDO DE R4 A LA DIRECCION APUNTADA POR R0

forever
        B       forever

